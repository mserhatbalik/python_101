# In programming indexes usually starts from 0

animals = ["cat", "dog", "penguin", "monkey", "donkey", "seagull", "dolphin", "eagle"]

# Accessing the second element in the list
print(animals[1])

# Accessing the fifth element in the list
print(animals[4])

# Accessing the last element in the list. This functionality will be useful in the future when we don't know the exact number of elements in a list
print(animals[-1])

# Accessing the 3rd last element in the list.
print(animals[-3])