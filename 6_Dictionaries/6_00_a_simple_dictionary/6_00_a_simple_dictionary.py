# Dictionaries allows us to store more complex information in a relational way, which is more in line with real world applications
# Below is a simple dictionary that stores properties about alien_0

# Data inside a dictionary represents "key-value" pairs.
alien_0 = {"color": "green", "points": 5}

# When we provide a key, python gives back the value associated with that key.
# the key named "color" -> points to string "green"
print(alien_0["color"])

# the key named "points" -> points to integer 5
print(alien_0["points"])  # When we provide a key, python gives back the value associated with that key.