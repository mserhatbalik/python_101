# Adding new key-value pairs in dictionaries are similar to adding new items to a list

alien_0 = {"color": "green", "points": 5}
alien_0["x-position"] = 0
alien_0["y-position"] = 25

print(f"You've shot the {alien_0["color"]} alien at position {alien_0["x-position"]}-{alien_0["y-position"]} for {alien_0["points"]} points!")