# 1.3. Install Visual Studio Code
# Visual Studio Code is a code editor developed by Microsoft.
# It is available for Windows, Mac and Linux.
# You can download it from https://code.visualstudio.com/
#
# Once you have downloaded it, you can run it by double-clicking
# the icon on your desktop.
#
# You can also run it from the terminal by typing "code"
# on your terminal.